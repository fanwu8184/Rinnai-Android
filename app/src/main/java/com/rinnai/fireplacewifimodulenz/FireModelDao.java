package com.rinnai.fireplacewifimodulenz;

public class FireModelDao {

    private String fireModel;
    private String fireType;
    private String fireCountry;

    public String getFireModel() {
        return fireModel;
    }

    public void setFireModel(String fireModel) {
        this.fireModel = fireModel;
    }

    public String getFireType() {
        return fireType;
    }

    public void setFireType(String fireType) {
        this.fireType = fireType;
    }

    public String getFireCountry() {
        return fireCountry;
    }

    public void setFireCountry(String fireCountry) {
        this.fireCountry = fireCountry;
    }
}
